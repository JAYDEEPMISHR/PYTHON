# If not follows poor than replace substring with Good

def not_poor(str1):                                         # Define not_poor function
  snot = str1.find('not')
  spoor = str1.find('poor')
  

  if spoor > snot and snot>0 and spoor>0:
    str1 = str1.replace(str1[snot:(spoor+4)], 'good')
    return str1
  else:
    return str1
print(not_poor('This song is not that poor!'))

